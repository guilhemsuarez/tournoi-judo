package fr.martres.judo.tournoi.core.rapport.bo;

public class RapportResultat {
	private String nom;
	private String prenom;
	private String grade;
	private String position;
	private String nbCombattant;
	private String nbPoint;
	/**
	 * @return the nom
	 */
	public String getNom() {
		return nom;
	}
	/**
	 * @param nom the nom to set
	 */
	public void setNom(String nom) {
		this.nom = nom;
	}
	
	/**
	 * @return the grade
	 */
	public String getGrade() {
		return grade;
	}
	/**
	 * @param grade the grade to set
	 */
	public void setGrade(String grade) {
		this.grade = grade;
	}
	/**
	 * @return the prenom
	 */
	public String getPrenom() {
		return prenom;
	}
	/**
	 * @param prenom the prenom to set
	 */
	public void setPrenom(String prenom) {
		this.prenom = prenom;
	}
	/**
	 * @return the position
	 */
	public String getPosition() {
		return position;
	}
	/**
	 * @param position the position to set
	 */
	public void setPosition(String position) {
		this.position = position;
	}
	/**
	 * @return the nbCombattant
	 */
	public String getNbCombattant() {
		return nbCombattant;
	}
	/**
	 * @param nbCombattant the nbCombattant to set
	 */
	public void setNbCombattant(String nbCombattant) {
		this.nbCombattant = nbCombattant;
	}
	/**
	 * @return the nbPoint
	 */
	public String getNbPoint() {
		return nbPoint;
	}
	/**
	 * @param nbPoint the nbPoint to set
	 */
	public void setNbPoint(String nbPoint) {
		this.nbPoint = nbPoint;
	}
	
}
