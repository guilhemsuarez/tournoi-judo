package fr.martres.judo.tournoi.core.rapport.bo;


public class RapportCompetition {

	private String nom;
	private String ville;
	private String date;
	private String nbJudokas;
	private String nbPoints;
	
	/**
	 * @return the nbJudokas
	 */
	public String getNbJudokas() {
		return nbJudokas;
	}
	/**
	 * @param nbJudokas the nbJudokas to set
	 */
	public void setNbJudokas(String nbJudokas) {
		this.nbJudokas = nbJudokas;
	}
	/**
	 * @return the nbPoints
	 */
	public String getNbPoints() {
		return nbPoints;
	}
	/**
	 * @param nbPoints the nbPoints to set
	 */
	public void setNbPoints(String nbPoints) {
		this.nbPoints = nbPoints;
	}
	/**
	 * @return the nom
	 */
	public String getNom() {
		return nom;
	}
	/**
	 * @param nom the nom to set
	 */
	public void setNom(String nom) {
		this.nom = nom;
	}
	/**
	 * @return the ville
	 */
	public String getVille() {
		return ville;
	}
	/**
	 * @param ville the ville to set
	 */
	public void setVille(String ville) {
		this.ville = ville;
	}
	/**
	 * @return the date
	 */
	public String getDate() {
		return date;
	}
	/**
	 * @param date the date to set
	 */
	public void setDate(String date) {
		this.date = date;
	}
	
	
	

}
